//! # CMA Rust Library
//! 
//! A comprehensive library providing internationalization (i18n), reactive programming patterns (rx),
//! and tracing capabilities for Rust applications.
//! 
//! ## Modules
//! 
//! - `i18n`: Internationalization support with locale management and translation loading
//! - `rx`: Reactive programming patterns inspired by RxJS for async data streams
//! - `trace`: Logging and tracing system with multiple handlers and configurable levels

/// Internationalization module providing locale management and translation capabilities.
pub mod i18n;

/// Reactive programming module with Observable patterns inspired by RxJS.
pub mod rx;

/// Tracing and logging module with configurable handlers and levels.
pub mod trace;
